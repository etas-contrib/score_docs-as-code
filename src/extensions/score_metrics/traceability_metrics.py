"""Shared traceability metric calculations for CI checks and dashboards."""

from __future__ import annotations

from pathlib import Path
from typing import Any

from score_metamodel import ScoreNeedType
from score_metamodel.yaml_parser import load_metamodel_data
from sphinx.application import Sphinx
from sphinx_needs.data import NeedsView, SphinxNeedsData
from sphinx_needs.need_item import NeedItem

CALCULATED_METRICS: dict[str, object] = {}


def get_need_types_by_tags(needs: list[ScoreNeedType], tags: set[str]) -> list[str]:
    """
    Takes a list of 'ScoreNeedTypes' and filters out any that have one or more of the
    specified tags given.
    """
    found_need_types: list[str] = []
    for need_type in needs:
        found_tag_set = set(need_type["tags"])
        if tags.intersection(found_tag_set):
            found_need_types.append(need_type["directive"])
    return found_need_types


def is_non_empty(value: object) -> bool:
    """Return True if value should be treated as present for traceability checks."""
    if isinstance(value, str):
        return bool(value.strip())
    return bool(value)


def safe_percent(numerator: int, denominator: int) -> float:
    """Return percentage in range [0, 100], treating empty denominator as 100%."""
    if denominator == 0:
        return 100.0
    return (numerator / denominator) * 100.0


def calculate_requirement_metrics(
    current_requirement_needs: list[NeedItem],
) -> dict[str, int | float]:
    """Calculate requirement traceability statistics for links and completeness."""
    total = len(current_requirement_needs)
    reqs_with_code_link = 0
    reqs_with_test_link = 0
    reqs_fully_linked = 0

    for need in current_requirement_needs:
        # Sourcecode link check
        if is_non_empty(need.get("source_code_link")):
            reqs_with_code_link += 1

        # Testlink check
        if is_non_empty(need.get("testlink")):
            reqs_with_test_link += 1
        # Negative check (both missing)
        if is_non_empty(need.get("testlink")) and is_non_empty(
            need.get("source_code_link")
        ):
            reqs_fully_linked += 1

    return {
        "total": total,
        "with_code_link": reqs_with_code_link,
        "with_test_link": reqs_with_test_link,
        "fully_linked": reqs_fully_linked,
        "with_code_link_pct": safe_percent(reqs_with_code_link, total),
        "with_test_link_pct": safe_percent(reqs_with_test_link, total),
        "fully_linked_pct": safe_percent(reqs_fully_linked, total),
    }


def calculate_test_metrics(
    test_needs: list[NeedItem], all_needs: NeedsView
) -> dict[str, object]:
    """Calculate testcase linkage and broken testcase-reference statistics."""
    tests_total = len(test_needs)
    tests_linked = 0
    broken_references: list[dict[str, str]] = []

    for test_need in test_needs:
        test_id = str(test_need.get("id"))
        partially: list[str] = test_need.get("partially_verifies", [])
        fully: list[str] = test_need.get("fully_verifies", [])
        refs = set(partially + fully)
        if refs:
            tests_linked += 1
        for ref in refs:
            if ref not in all_needs:
                broken_references.append({"testcase": test_id, "missing_need": ref})

    return {
        "total": tests_total,
        "linked_to_requirements": tests_linked,
        "linked_to_requirements_pct": safe_percent(tests_linked, tests_total),
        "broken_references": broken_references,
    }


def calculate_full_need_metrics(app: Sphinx, include_external: bool):
    """
    Calculate all tracked metrics for requirements and tests.
    Will save the result in a global variable 'CALCULATED_METRICS'
    """
    #            ───────────────[ Getting configuration values ]───────────────
    global CALCULATED_METRICS
    all_needs: NeedsView = SphinxNeedsData(app.env).get_needs_view()

    raw_metamodel_path = app.config.score_metamodel_yaml
    override_path = Path(raw_metamodel_path) if raw_metamodel_path else None
    metamodel = load_metamodel_data(override_path)

    # We either get the types that should be considered from the configuration
    # If none are specified the need types declared in the Metamodel with
    # the tags 'requirement' are taken
    raw = getattr(app.config, "score_metrics_requirement_types", "").strip()
    filter_reqs = [t.strip() for t in raw.split(",") if t.strip()]
    if not filter_reqs:
        filter_reqs = get_need_types_by_tags(metamodel.needs_types, {"requirement"})
    #            ──────────────────[ Calculate Test Metrics ]──────────────────

    test_needs = list(all_needs.filter_types(["testcase"]).values())
    test_metrics = calculate_test_metrics(test_needs, all_needs)

    metrics_by_type: dict[str, dict[str, int | float]] = {}

    # Metrics accumulated over all requirements types
    overall_metrics: dict[str, Any] = {
        "total": 0,
        "with_code_link": 0,
        "with_test_link": 0,
        "fully_linked": 0,
    }

    #            ─────[ Calculating Metrics for each requirement_type ]───
    for req_type in sorted(filter_reqs):
        if include_external:
            needs_of_req_type = all_needs.filter_types([req_type])
        else:
            needs_of_req_type = all_needs.filter_types([req_type]).filter_is_external(
                False
            )
        # We do not care if there is no requirements of this type.
        if not list(needs_of_req_type.values()):
            continue
        req_metrics = calculate_requirement_metrics(list(needs_of_req_type.values()))
        overall_metrics["total"] += req_metrics["total"]
        overall_metrics["with_code_link"] += req_metrics["with_code_link"]
        overall_metrics["with_test_link"] += req_metrics["with_test_link"]
        overall_metrics["fully_linked"] += req_metrics["fully_linked"]
        metrics_by_type[req_type] = req_metrics
    # Calculating % of each category for the overall metrics
    overall_metrics["with_code_link_pct"] = safe_percent(
        overall_metrics["with_code_link"], overall_metrics["total"]
    )
    overall_metrics["with_test_link_pct"] = safe_percent(
        overall_metrics["with_test_link"], overall_metrics["total"]
    )
    overall_metrics["fully_linked_pct"] = safe_percent(
        overall_metrics["fully_linked"], overall_metrics["total"]
    )

    output: dict[str, object] = {
        "schema_version": "2",
        "generated_by": "sphinx_build",
        "overall_metrics": overall_metrics,
        "metrics_by_type": metrics_by_type,
        "tests": test_metrics,
    }
    # Save the metrics in a Global Variable to enable access from other parts.
    # Not a great solution but it is needed, as needpie filter functions for example
    # can not access 'app'.
    CALCULATED_METRICS.update(output)
